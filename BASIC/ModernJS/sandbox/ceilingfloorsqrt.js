/**
 * CEILING AND FLOOR
 * ROOT OF A NUMBER
 */

// Math.ceil(n)
console.log('Ceil:');
console.log(Math.ceil(0.60));
console.log(Math.ceil(0.40));
console.log(Math.ceil(5.10));
console.log(Math.ceil(-5.10));
console.log(Math.ceil(-5.90));

// Math.floor(n)
console.log('Floor:')
console.log(Math.floor(0.60));
console.log(Math.floor(0.4))
console.log(Math.floor(5.1))
console.log(Math.floor(-5.1))
console.log(Math.floor(-5.9));

// Square Root
